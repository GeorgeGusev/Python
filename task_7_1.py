def inch_to_sm(inch:float):
    results = inch*2.54
    print(f"Дюймы в сантиметры: {inch} --> {results}")

def sm_to_inch(sm:float):
    results = sm*0.393701
    print(f"Сантиметры в дюймы: {sm} --> {results}")

def mile_to_km(mile: float):
    results = mile * 1.60934
    print(f"Мили в километры: {mile} --> {results}")

def km_to_mile(km: float):
    results = km * 0.621371
    print(f"Километры в мили: {km} --> {results}")

